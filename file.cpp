#include "file.h"

/// @brief Моя функция суммирования двух целых чисел
/// @param a число а
/// @param b число б
/// @return сумма
int my_sum(int a, int b)
{
    return a + b;
}

/// @brief Моя функция умножения
/// @param a число а
/// @param b число б
/// @return произведение
int my_mult(int a, int b)
{
    return a * b;
}
